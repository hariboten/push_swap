#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t len)
{
	unsigned	char	*p1;
	unsigned	char	*p2;

	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	while (len--)
	{
		if (*p1 != *p2)
			return (*p1 - *p2);
		p1++;
		p2++;
	}
	return (0);
}
